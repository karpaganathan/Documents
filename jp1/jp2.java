package jp1;

public class jp2 {
	public static void main(String[]args) {
		System.out.println("Hii, how r u bro?");
	
		//integer
		int a, b, c;
		a=5;
		b=10;
		c=a+b;
		System.out.println("Addition is " +c);
		
		//float
		float myNum=5.75f;
		System.out.println(myNum);		
		
		//arithmetic operators
		 int a1 = 5 + 45;
		 System.out.println(a1);
		
		 //increment 
		 int x = 99;
		 ++x;
		 System.out.println(x);  
		 
		 //decrement
		 int x2 = 5;
		 --x2;
		 System.out.println(x2);
		 
		 //Modulus
		 int q = 5;
		 int w = 2;
		 System.out.println(q % w);
		 
		 //boolean
		 int e = 15;
		 int r = 10;
		 System.out.println(e > r);
		 
		System.out.println(x);
		
		//!=
		 int l = 50;
		 int s = 50;
		 System.out.println(l != s);
		 
		 //logical and
		 int m = 100;
		 System.out.println(m > 3 && m < 10);
		 
		 int n = 5;
		 System.out.println(n > 3 && n < 10);
		 
		 // logical or
		 int o = 5;
		 System.out.println(o > 5 || o < 10);
	}
	    

}

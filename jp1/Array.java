package jp1;

public class Array {
	public static void main (String[] args) {
		//normal array with length
		String[]fruits= {"mango","papaya","orange","apple","watermelon"};
		System.out.println(fruits.length);
		
		//using for statement
		for(String k:fruits) {
			System.out.println(k);
		}
		

}

}
